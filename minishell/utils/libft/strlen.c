#include "minishell.h"

int	ft_strlen(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] && s[i] != c)
	{
		if (s[i] == c)
		{
			return (i);
		}
		i++;
	}
	return (i);
}
